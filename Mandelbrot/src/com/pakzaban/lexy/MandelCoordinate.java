package com.pakzaban.lexy;

public class MandelCoordinate {
    private double a;
    private double b;

    public MandelCoordinate(double a, double b) {
        this.a = a;
        this.b = b;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }
}
